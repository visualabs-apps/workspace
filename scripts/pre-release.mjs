import fs from 'fs';
import path from 'path';
import readline from 'readline';
import { fileURLToPath } from 'url';
import { execSync } from 'child_process';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const packageJsonPath = path.resolve(__dirname, '../package.json');

// в”Ђв”Ђв”Ђ Helpers в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

function ask(rl, question) {
    return new Promise((resolve) => rl.question(question, resolve));
}

function bumpVersion(version, type) {
    const [major, minor, patch] = version.split('.').map(Number);
    if (type === 'major') return `${major + 1}.0.0`;
    if (type === 'minor') return `${major}.${minor + 1}.0`;
    if (type === 'patch') return `${major}.${minor}.${patch + 1}`;
    return version;
}

// в”Ђв”Ђв”Ђ Main в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

const rl = readline.createInterface({
    input: process.stdin,
    output: process.stdout,
});

const pkg = JSON.parse(fs.readFileSync(packageJsonPath, 'utf8'));
const currentVersion = pkg.version;

console.log('\nв•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—');
console.log('в•‘        рџљЂ  VisualBox Release        в•‘');
console.log('в•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ\n');
console.log(`рџ“Њ Current version: ${currentVersion}\n`);

// в”Ђв”Ђв”Ђ Step 1: Version bump в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

console.log('Choose version bump type:');
console.log('  [1] Keep current  в†’  ' + currentVersion);
console.log('  [2] Patch         в†’  ' + bumpVersion(currentVersion, 'patch') + '  (bug fixes)');
console.log('  [3] Minor         в†’  ' + bumpVersion(currentVersion, 'minor') + '  (new features)');
console.log('  [4] Major         в†’  ' + bumpVersion(currentVersion, 'major') + '  (breaking changes)');
console.log('  [5] Custom        в†’  enter manually\n');

const bumpChoice = (await ask(rl, 'Your choice [1-5]: ')).trim();

let newVersion = currentVersion;

if (bumpChoice === '2') {
    newVersion = bumpVersion(currentVersion, 'patch');
} else if (bumpChoice === '3') {
    newVersion = bumpVersion(currentVersion, 'minor');
} else if (bumpChoice === '4') {
    newVersion = bumpVersion(currentVersion, 'major');
} else if (bumpChoice === '5') {
    const custom = (await ask(rl, 'Enter custom version (e.g. 1.2.3): ')).trim();
    if (/^\d+\.\d+\.\d+$/.test(custom)) {
        newVersion = custom;
    } else {
        console.error('вќЊ Invalid version format. Must be x.y.z');
        rl.close();
        process.exit(1);
    }
}

// в”Ђв”Ђв”Ђ Step 2: Release notes в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

console.log('\nрџ“ќ Enter release notes/description.');
console.log('   (You can use multiple lines. Type END on a new line to finish.)\n');

let notes = '';
let line = '';
while ((line = (await ask(rl, '')).trim()) !== 'END') {
    notes += (notes ? '\n' : '') + line;
}

if (!notes.trim()) {
    notes = `Release version ${newVersion}`;
}

rl.close();

// в”Ђв”Ђв”Ђ Step 3: Confirm в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

console.log('\nв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ');
console.log(`  Version  : ${currentVersion} в†’ ${newVersion}`);
console.log(`  Notes    :\n${notes.split('\n').map(l => '    ' + l).join('\n')}`);
console.log('в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ\n');

// в”Ђв”Ђв”Ђ Step 4: Bump version in files в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

if (newVersion !== currentVersion) {
    console.log(`рџ”„ Bumping version to ${newVersion}...`);

    // package.json update
    pkg.version = newVersion;
    fs.writeFileSync(packageJsonPath, JSON.stringify(pkg, null, 2));
    console.log('   вњ… package.json updated');
}

// в”Ђв”Ђв”Ђ Step 5: Write notes to temp file for release-updater.js to pick up в”Ђв”Ђв”Ђв”Ђв”Ђ

const tempNotesPath = path.resolve(__dirname, '.release-notes.tmp');
fs.writeFileSync(tempNotesPath, JSON.stringify({ version: newVersion, notes }, null, 2));
console.log('\nрџ“‹ Release notes saved.');

// в”Ђв”Ђв”Ђ Step 6: Run the actual build в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

console.log('\nрџЏ—пёЏ  Starting Electron production build...\n');
try {
    execSync('npm run dist:win', { stdio: 'inherit', cwd: path.resolve(__dirname, '..') });
} catch (e) {
    console.error('\nвќЊ Build failed! Aborting release.');
    process.exit(1);
}

// в”Ђв”Ђв”Ђ Step 7: Run the updater script в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

console.log('\nрџ“¦ Updating release files...\n');
try {
    execSync('node scripts/release-updater.js', { stdio: 'inherit', cwd: path.resolve(__dirname, '..') });
} catch (e) {
    console.error('\nвќЊ Release updater failed!');
    process.exit(1);
}

// в”Ђв”Ђв”Ђ Cleanup в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

if (fs.existsSync(tempNotesPath)) fs.unlinkSync(tempNotesPath);

console.log('\nрџЋ‰ Release complete!');
console.log(`   Version: ${newVersion}`);
